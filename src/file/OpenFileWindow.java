    private void bOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bOKActionPerformed
        // TODO add your handling code here:
        //Se nao selecionou nenhum arquivo, exibe uma msg para selecionar
        if (txtRoute.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Select a file");
        } else {
            //rootInterface.enableField(txtRoute.getText());
            ArrayList<GraphicComponent> c = new ArrayList<>();            
            try {
                FileInputStream fi = new FileInputStream(txtRoute.getText());
                ObjectInputStream oi = new ObjectInputStream(fi);
                c = (ArrayList<GraphicComponent>) oi.readObject();
                oi.close();                
            } catch (IOException | ClassNotFoundException e) {
                System.out.println(e);
            }

            try {
                BufferedReader br = new BufferedReader(new FileReader("window.config"));
                StringBuilder sb = new StringBuilder();
                Double xMin = Double.parseDouble(br.readLine());
                Double xMax = Double.parseDouble(br.readLine());
                Double yMin = Double.parseDouble(br.readLine());
                Double yMax = Double.parseDouble(br.readLine());
                int width = Integer.parseInt(br.readLine());
                int height = Integer.parseInt(br.readLine());

                wp.changed(xMin, xMax, yMin, yMax, width, height);

            } catch (Exception ex) {
                Logger.getLogger(OpenFileWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            rootInterface.openFile(c, txtRoute.getText());
            dispose();
        }
    }//GEN-LAST:event_bOKActionPerformed

    private void bSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSelectActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();

        fc.setFileFilter(new FileNameExtensionFilter("APEGraphics (*.CG1)", "cg1"));
        int reply = fc.showOpenDialog(this);

        if (reply == JFileChooser.APPROVE_OPTION) {
            txtRoute.setText(fc.getSelectedFile().toString());
        }
    }//GEN-LAST:event_bSelectActionPerformed

